        public void refreshcombo() {
            try {
                DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
                Class.forName("org.apache.derby.jdbc.EmbeddedDriver");//prueba es base de datos
            String url = "jdbc:derby:C:/Edson;create=true;user=edson;password=edson";
            Connection conn = DriverManager.getConnection(url);
            //conn.setSchema("EDSON");
                Statement s = conn.createStatement();
                s.execute("SELECT * FROM CLIENTE");
                ResultSet rs = s.getResultSet();

                while (rs.next()) {
                    modeloCombo.addElement(rs.getObject("NOMBRE"));
                }
                conn.close();
                cbxnombre.setModel(modeloCombo);

            } catch (Exception e) {
            }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void cbxnombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxnombreActionPerformed
        String nombre = cbxnombre.getSelectedItem().toString();

        try {
            DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");//prueba es base de datos
            String url = "jdbc:derby:C:/Edson;create=true;user=edson;password=edson";
            Connection conn = DriverManager.getConnection(url);
            //conn.setSchema("EDSON");
            Statement s = conn.createStatement();
            s.execute("SELECT * FROM CLIENTE where NOMBRE = '" + nombre + "'");
            ResultSet rs = s.getResultSet();

            while (rs.next()) {

                txtRut.setText(rs.getString("RUT"));
                txtRazon.setText(rs.getString("RAZON"));
                txtGiro.setText(rs.getString("GIRO"));
                txtDireccion.setText(rs.getString("DIRECCION"));
                txtComuna.setText(rs.getString("COMUNA"));
                txtCiudad.setText(rs.getString("CIUDAD"));
                txtFono.setText(rs.getString("FONO"));
                txtEmail.setText(rs.getString("EMAIL"));
                txtWeb.setText(rs.getString("WEB"));

            }
           
            conn.close();
            
            

        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_cbxnombreActionPerformed

